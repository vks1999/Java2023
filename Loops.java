public class Loops {
    public static void main(String[] args) {

        // For Loop

        // i++ => i = i + 1
        /*
         * for (int i = 0; i < 11; i++) {
         * System.out.println(i);
         * }
         */

        // While Loop

        int i = 0;

        /*
         * while (i < 11) {
         * System.out.println(i);
         * i++; // i = i + 1
         * }
         */

        // Do - while Loop

        do {
            System.out.println(i);
            i++;
        } while (i < 11);
    }
}
